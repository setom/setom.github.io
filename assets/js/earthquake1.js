//Thanks to: http://slides.com/brittaricker-1/google-maps#/3/1
function getCircle(magnitude) {
    var circle = {
        path: google.maps.SymbolPath.CIRCLE,
        fillColor: 'red',
        fillOpacity: .2,
        scale: Math.pow(2, magnitude) / 2,
        strokeColor: 'white',
        strokeWeight: .5
    };
    return circle;
}




